#include "math_private.h"

/* __ieee754_sinh(x)
 * Method :
 * mathematically sinh(x) if defined to be (exp(x)-exp(-x))/2
 *	1. Replace x by |x| (sinh(-x) = -sinh(x)).
 *	2.
 *		                                    E + E/(E+1)
 *	    0        <= x <= 22     :  sinh(x) := --------------, E=expm1(x)
 *			       			        2
 *
 *	    22       <= x <= lnovft :  sinh(x) := exp(x)/2
 *	    lnovft   <= x <= ln2ovft:  sinh(x) := exp(x/2)/2 * exp(x/2)
 *	    ln2ovft  <  x	    :  sinh(x) := x*shuge (overflow)
 *
 * Special cases:
 *	sinh(x) is |x| if x is +INF, -INF, or NaN.
 *	only sinh(0)=0 is exact for finite x.
 */
double __hide_ieee754_sinh(double x)
{
   double t, w, h;
   int ix, jx;
   unsigned lx;

   /* High word of |x|. */
   jx = GET_HI(x);
   ix = jx & 0x7fffffff;

   /* x is INF or NaN */
   if(ix >= 0x7ff00000)
      return x;

   h = 0.5;
   if(jx < 0)
      h = -h;
   /* |x| in [0,22], return sign(x)*0.5*(E+E/(E+1))) */
   if(ix < 0x40360000)
   {                      /* |x|<22 */
      if(ix < 0x3e300000) /* |x|<2**-28 */
         if(shuge + x > one)
            return x; /* sinh(tiny) = tiny with inexact */
      t = expm1(fabs(x));
      if(ix < 0x3ff00000)
         return h * (2.0 * t - t * t / (t + one));
      return h * (t + t / (t + one));
   }

   /* |x| in [22, log(maxdouble)] return 0.5*exp(|x|) */
   if(ix < 0x40862E42)
      return h * __hide_ieee754_exp(fabs(x));

   /* |x| in [log(maxdouble), overflowthresold] */
   lx = *((((*(unsigned*)&one) >> 29)) + (unsigned*)&x);
   if(ix < 0x408633CE || (ix == 0x408633ce) && (lx <= (unsigned)0x8fb9f87d))
   {
      w = __hide_ieee754_exp(0.5 * fabs(x));
      t = h * w;
      return t * w;
   }

   /* |x| > overflowthresold, sinh(x) overflow */
   return x * shuge;
}

/*
 * wrapper sinh(x)
 */
double sinh(double x) /* wrapper sinh */
{
#ifdef _IEEE_LIBM
   return __hide_ieee754_sinh(x);
#else
   double z;
   z = __hide_ieee754_sinh(x);
   if(_LIB_VERSION == _IEEE_)
      return z;
   if(!finite(z) && finite(x))
   {
      return __hide_kernel_standard(x, x, 25); /* sinh overflow */
   }
   else
      return z;
#endif
}
